VUint32 *pStatus = (VUint32 *)0x40011000;
VUint32 *pSend = (VUint32 *)0x40011004;
int32 rt_send_byte(int8 byte)
{
    while (0 == ((*pStatus) & 0x00000080));
    *pSend = byte;
 
    return byte;
}

int32 DebugSendByte(int8 byte)
{
    return rt_send_byte(byte);
}



